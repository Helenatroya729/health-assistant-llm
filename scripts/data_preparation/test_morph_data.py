import json
import re
import pymorphy2

# инициализация анализатора pymorphy2
morph = pymorphy2.MorphAnalyzer()

def is_valid_word(word):
   # проверяем, есть ли слово в словаре 
    parsed = morph.parse(word)
    if not parsed:
        return False  
    
    normal_form = parsed[0].normal_form
    pos = parsed[0].tag.POS 

    if pos is None:  
        return False

    return True

def tokenize(text):
    # разбиваем на части
    return re.findall(r'\b[а-яА-ЯёЁ]+\b', text.lower())

file_path = "combined_dataset.jsonl"

total_lines = 0
total_words = 0
total_violations = 0

errors = []

with open(file_path, "r", encoding="utf-8-sig") as file:
    for line_number, line in enumerate(file, start=1):
        line = line.strip()
        if not line:
            continue
        try:
            entry = json.loads(line)
            total_lines += 1 

            if "completion" not in entry:
                print(f"Ошибка: отсутствует ключ 'completion' в строке {line_number}")
                continue

            completion_text = entry["completion"]
            words_in_text = tokenize(completion_text)
            total_words += len(words_in_text)  

            # проверяем слова по pymorphy2
            unknown_words = [word for word in words_in_text if not is_valid_word(word)]
            total_violations += len(unknown_words)  

            if unknown_words:
                errors.append(f"Ошибка в строке {line_number}: возможно нарушение терминологии: {', '.join(unknown_words)}")

        except json.JSONDecodeError as e:
            print(f"Ошибка JSON в строке {line_number}: {e}")
            print(f"Строка: {line}")

# Вывод 
if errors:
    print("\n=== НАЙДЕННЫЕ ОШИБКИ ===")
    for error in errors: 
        print(error)

print(f"Проверено строк: {total_lines}")
print(f"Проверено слов: {total_words}")
print(f"Обнаружено нарушений терминологии: {total_violations}")



